const { execSync } = require('child_process');
const { writeFileSync } = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');

let commitSha;
try {
  commitSha = execSync('git rev-parse HEAD', { cwd: repoRoot, encoding: 'utf8' }).trim();
} catch {
  commitSha = 'unknown';
}

const output = `// Auto-generated by scripts/generate-build-info.js — do not edit manually
export const FRONTEND_GIT_SHA = '${commitSha}';
`;

const dest = path.join(repoRoot, 'apps', 'frontend', 'src', 'build-info.ts');
writeFileSync(dest, output, 'utf8');

console.log(`Generated build-info.ts with SHA ${commitSha}`);
