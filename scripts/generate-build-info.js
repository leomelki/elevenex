#!/usr/bin/env node
const { execSync } = require('child_process');
const { writeFileSync, mkdirSync } = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');

function resolveSha() {
  const fromEnv =
    process.env.GIT_SHA ||
    process.env.GITHUB_SHA ||
    process.env.BACKEND_SHA ||
    process.env.SOURCE_COMMIT;
  if (fromEnv && fromEnv.trim()) return fromEnv.trim();
  try {
    return execSync('git rev-parse HEAD', { cwd: repoRoot, encoding: 'utf8' }).trim();
  } catch {
    return 'unknown';
  }
}

const sha = resolveSha();
const header = '// Auto-generated by scripts/generate-build-info.js — do not edit manually\n';

const targets = [
  {
    file: path.join(repoRoot, 'apps', 'backend', 'src', 'generated', 'git-sha.ts'),
    contents: `${header}export const GIT_SHA = '${sha}';\n`,
  },
  {
    file: path.join(repoRoot, 'apps', 'frontend', 'src', 'build-info.ts'),
    contents: `${header}export const FRONTEND_GIT_SHA = '${sha}';\n`,
  },
];

for (const { file, contents } of targets) {
  mkdirSync(path.dirname(file), { recursive: true });
  writeFileSync(file, contents, 'utf8');
  console.log(`[build-info] wrote ${path.relative(repoRoot, file)} (${sha})`);
}
